// Close menu and scroll to section when a nav link is clicked
document.querySelectorAll(".nav_menu_link").forEach(function (link) {
  link.addEventListener("click", function (e) {
    const href = this.getAttribute("href");
    // Only intercept same-page anchor links (starting with #)
    if (href && href.startsWith("#")) {
      e.preventDefault();
      const target = document.querySelector(href);
      closeMenu();
      if (target && lenis) {
        // Small delay lets the menu slide out before scroll begins
        setTimeout(function () { lenis.scrollTo(target, { offset: -80 }); }, 350);
      }
    } else {
      // External / different-page link — just close the menu, let the browser navigate
      closeMenu();
    }
  });
});

// ─── ANCHOR LINK SMOOTH SCROLL (all hrefs starting with #) ───────────────────
document.querySelectorAll('a[href^="#"]').forEach(function (anchor) {
  // Skip nav menu links — handled above
  if (anchor.classList.contains("nav_menu_link")) return;

  anchor.addEventListener("click", function (e) {
    const href = this.getAttribute("href");
    if (href === "#") {
      e.preventDefault();
      if (lenis) lenis.scrollTo(0);
      return;
    }
    const target = document.querySelector(href);
    if (target) {
      e.preventDefault();
      if (lenis) lenis.scrollTo(target, { offset: -80 });
    }
  });
});

