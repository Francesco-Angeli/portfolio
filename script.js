function homeLoader() {
  const heroImgWrap = $("[hero_img_wrap]");
  const heroImg     = $("[hero_img]");
  const navWrap     = $("[nav_wrap]");

  const tl = gsap.timeline({
    delay: 0.5,
    defaults: { duration: 1.5, ease: "power4.inOut" }
  });

  // 1. Reveal the page
  tl.set('[page-wrap="home"]', { autoAlpha: 1 });

  // 2. Hero image wrapper: rises from below with fade
  tl.from(heroImgWrap, {
    yPercent:  50,
    autoAlpha: 0,
    ease:      "power4.out"
  });

  // 3. Inner image: counter-parallax from above (simultaneous with step 2)
  tl.from(heroImg, {
    yPercent: -30,
    ease:     "power4.out"
  }, 0);

  // 4. Clip-path: rectangle at center opens to full frame
  tl.from(heroImgWrap, {
    clipPath: "polygon(40% 25%, 60% 25%, 60% 75%, 40% 75%)",
    duration: 2
  }, ">-1");

  // 5. Letters A·N·G·E·L·I: rise from below the overflow:hidden edge
  tl.from(".hero_text", {
    yPercent: 100,
    stagger:  0.05,
    duration: 1,
    ease:     "power4.out"
  }, "<1.2");

  // 5b. "FRANCESCO" label fades + rises alongside the letters
  tl.from(".hero_name_first", {
    autoAlpha: 0,
    yPercent:  40,
    duration:  1,
    ease:      "power4.out"
  }, "<");

  // 6. Nav bar: fades in alongside the letters
  tl.from(navWrap, {
    autoAlpha: 0,
    duration:  1
  }, "<");
}

